// Creating map options
var mapOptions = {
center: [ 30.615418157147218, -96.34106768341667],
zoom: 18
}
// Creating a map object
var map = new L.map('map', mapOptions);

// Creating a Layer object
var layer = new L.TileLayer('http://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png');

// Adding layer to the map
map.addLayer(layer);

// Creating latlng object
var latlang1 = [[
[ 30.627955187175957, -96.33657659462108],
[ 30.601812538806897, -96.33657659462108],
[ 30.601812538806897, -96.30929263801701],
[ 30.627955187175957, -96.30929263801701],
]];
// Creating multi polygon options
var multiPolygonOptions = {color:'red', weight:20};

// Creating multi polygons
var multipolygon = L.multiPolygon(latlang1 , multiPolygonOptions);

// Adding multi polygon to map
multipolygon.addTo(map);
